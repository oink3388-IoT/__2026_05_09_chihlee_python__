random.seed(42)
names = []
used = set()
count = 0
i = 0

while count < 200:
    surname = random.choice(surnames)
    if i % 2 == 0:
        given = random.choice(male_names)
        gender = '男'
    else:
        given = random.choice(female_names)
        gender = '女'
    
    full_name = surname + given
    key = full_name + gender
    
    if key not in used:
        used.add(key)
        names.append((count + 1, full_name, gender))
        count += 1
    i += 1
    
    if i > 1000:
        break

with open('chinese_names.csv', 'w', newline='', encoding='utf-8-sig') as f:
    writer = csv.writer(f)
    writer.writerow(['編號', '姓名', '性別'])
    writer.writerows(names)

